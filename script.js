let currentQuestionIndex = 0;
let questionHTMLBox = document.querySelectorAll('.question')[0];
let form = document.querySelector('form')

let answers;

let responses = []

let renderQuestion = () => {
    if (currentQuestionIndex === questionsAnswers.length) {
        questionHTMLBox.innerHTML += "<h1>Hooray! The quiz is complete!<br><br></h1>"

        questionHTMLBox.innerHTML += "Your answers: <br>"
        responses.map((response, index) => {
            questionHTMLBox.innerHTML += `${index+1}: ${response}<br>`
        })

        return
    }
    answers = questionsAnswers[currentQuestionIndex].Answers;

    questionHTMLBox.innerHTML += `
    <h2>
    ${currentQuestionIndex + 1}. 
    ${questionsAnswers[currentQuestionIndex].Question}
    </h2>
    <ul>
    ${
        answers.map((answer, index) => {
            return `<li><input type="radio" name="selectedAnswer" value="${index}">${answer}</li>`
        })
    }
    </ul>
    `;
}

window.onload = () => {
    renderQuestion();
}

form.onsubmit = (e) => {
    e.preventDefault();

    var answerRadios = document.getElementsByName('selectedAnswer');

    let answerSelected = false
    for (let i = 0; i < answerRadios.length; i++) {
        if(answerRadios[i].checked) {
            if (questionsAnswers[currentQuestionIndex]["Correct Answer"] !== "") {
                console.log(answerRadios[i].value)
                if (answerRadios[i].value == questionsAnswers[currentQuestionIndex]["Correct Answer"]) {
                    questionHTMLBox.innerHTML += "Yay, you're correct! Smart Girl!<br>"
                } else {
                    questionHTMLBox.innerHTML += `Nice try! The correct answer is ${questionsAnswers[currentQuestionIndex].Answers[questionsAnswers[currentQuestionIndex]["Correct Answer"]]}<br>`
                }
            } else {
                questionHTMLBox.innerHTML += "Whatever you selected is correct! You selected it with your pure soul!<br>"
            }           
            responses.push(questionsAnswers[currentQuestionIndex].Answers[answerRadios[i].value])
            questionHTMLBox.innerHTML += questionsAnswers[currentQuestionIndex]["Secret Message"] + '<br>'
            answerSelected = true
        }
    }
    if (!answerSelected) {
        questionHTMLBox.innerHTML += "Please select an answer beautiful mind!<br>"
        return
    }
    let timeleft = questionsAnswers[currentQuestionIndex]["Read Interval"]
    questionHTMLBox.innerHTML += `<br>Heading to next question in <span id='timeLeft'>${timeleft}</span> seconds`
    let timeleftHTML = document.querySelector('#timeLeft');
    setInterval(() => {
        timeleftHTML.innerHTML = timeleft;

        if(timeleft === 0) {
            currentQuestionIndex += 1;
            questionHTMLBox.innerHTML = ""
            renderQuestion();
        }
        timeleft -= 1;
    }, 1000);
    console.log(responses)
}